#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = ft_strlen(s);
	str = (char *)s;
	while (i >= 0)
	{
		if (str[i] == (unsigned char)c)
		{
			str += i;
			return (str);
		}
		i--;
	}
	return (NULL);
}
