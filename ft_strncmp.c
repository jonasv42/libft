#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n > 0 && (*s1 == '\0' || *s2 == '\0'))
		return (s1[i] - s2[i]);
	while (i < n && *s1 && *s2)
	{
		if (s2[i] != s1[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
